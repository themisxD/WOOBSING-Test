     */
    public function store(Request $request)
    {
        $data  = $request->validate([
            'name' => 'required|max:50',
            'guard_name' => 'required',
        ]);

        $role = Permission::create($data);
        $role->save();

        return redirect()->route('admin.permissions.index');
